class CollectionTask extends RecursiveTask<Integer>{
    private List<Integer> list;
    private int result;

    public CollectionTask(List<Integer> list) {
        this.list = list;
    }

    public int getResult() {
        return result;
    }

    public void setResult(int result) {
        this.result = result;
    }

    @Override
    protected Integer compute() {
        int listLength = list.size();
        if (listLength <= 10){
            for(Integer i : list){
                result = result + i;
            }
        }else {
            int middle = listLength/2;
            List<Integer> list1 =  list.subList(0,middle);
            List<Integer> list2 =  list.subList(middle,listLength);
            CollectionTask collectionTask1 = new CollectionTask(list1);
            CollectionTask collectionTask2 = new CollectionTask(list2);
            collectionTask1.fork();
            collectionTask2.fork();
            result = collectionTask1.join()+collectionTask2.join();
        }
        return result;
    }
}

